import UIKit

extension UIFont {
    
    static func helvetica(size: CGFloat) -> UIFont {
        
        return UIFont(name: "HelveticaNeue-Bold", size: size)!
    }
    static func helveticaThin(size: CGFloat) -> UIFont {
        
        return UIFont(name: "HelveticaNeue-Thin", size: size)!
    }
    
}
